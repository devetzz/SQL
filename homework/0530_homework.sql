--[문제 1] EMPLOYEES 테이블에 등록된 총사원의 수와 급여의 합, 급여의 평균을 변수에 대입하여 출력하 여 보자.
DECLARE
    EMP_SIZE NUMBER;
    TOTAL_SALARY EMPLOYEES.SALARY%TYPE;
    AVG_SALARY EMPLOYEES.SALARY%TYPE;
BEGIN
    SELECT COUNT(*), SUM(SALARY), AVG(SALARY)
    INTO EMP_SIZE, TOTAL_SALARY, AVG_SALARY
    FROM EMPLOYEES;
    
    DBMS_OUTPUT.PUT_LINE('총사원의 수 : '||EMP_SIZE);
    DBMS_OUTPUT.PUT_LINE('급여의 합 : '||TOTAL_SALARY);
    DBMS_OUTPUT.PUT_LINE('급여의 평균 : '||AVG_SALARY);
END;
